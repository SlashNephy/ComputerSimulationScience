program program_2_1
  implicit none
  integer :: i, j, k, l, m, n
  real :: a1, a2, a3, a4
  complex :: z1, z2, z3

  i = 8 / 3
  j = 1 / 3
  k = -5 / 3
  l = -1 / 3
  m = 8.0 / 3.0 ! 8.0 / 3.0 -> 2.7 -> 2
  n = 8 / 3.0 ! 8 -> 8.0
  write(6, 600) i, j, k, l, m, n
600 format("i=", i2, ", j=", i2, ", k=", i2, ", l=", i2, ", m=", i2, ", n=", i2)

  a1 = 8 / 3 ! 8 / 3 -> 2 -> 2.0 
  a2 = 8.0 / 3 ! 8.0 / 3 -> 8.0 / 3.0 -> 2.7
  a3 = 2.0 * (8 / 3) ! 2.0 * (8 / 3) -> 2.0 * 2 -> 2.0 * 2.0 -> 4.0
  a4 = 2.0 * (8.0 / 3) ! 2.0 * (8.0 / 3) -> 2.0 * (8.0 / 3.0) -> 5.3
  write(6, 610) a1, a2, a3, a4
610 format("a1=", e9.2, ", a2=", e9.2, ", a3=", e9.2, ", a4=", e9.2)
  
  z1 = 2 ! 2 -> 2.0
  z2 = -3.0
  z3 = 2 * (3.0, -1.0) ! 2 * 3.0 -> 2.0 * 3.0, 2 * -1.0 -> 2.0 * -1.0
  write(6, 620) z1, z2, z3
620 format("z1=", 2e9.2, ", z2=", 2e9.2, ", z3=", 2e9.2)
end program program_2_1
